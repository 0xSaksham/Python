my_favourite_places = ['Maldives', 'Banff', 'America', 'Canada','Saint Louis']

# Simple Print Command
print(my_favourite_places)

#use of Sorted
print(sorted(my_favourite_places))

print(my_favourite_places)

#use of Sorted in reverse
print(sorted(my_favourite_places, reverse=True))

print(my_favourite_places)

#Reverse Function use
my_favourite_places.reverse()
print(my_favourite_places)

#Double Reverse = Original list
my_favourite_places.reverse()
print(my_favourite_places)

#Use of Sort()
my_favourite_places.sort()
print(my_favourite_places)

#Use of Sort() reverse

my_favourite_places.sort(reverse=True)
print(my_favourite_places)