#Ascending Sort by Default
cars = ['toyota', 'subaru', 'audi']

cars.sort()
print(cars)

# Length of a list
print(len(cars))

numbers = [9,8,7,6,5,4,3,1]

numbers.sort()
print(numbers)

#Descending Sort
cars.sort(reverse=True)
print(cars)

# Temporary Sort

print(sorted(cars))

# Temporary Sort (in Reverse)
print(sorted(cars ,reverse=True))



